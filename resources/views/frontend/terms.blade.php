<h2>Sobre la página</h2>

<p>Este sitio web fue diseñado por el Gobierno de la CDMX y tienen por objeto regular la forma y términos para la utilización del sistema electrónico de información pública gubernamental, denominado CONTRATACIONES ABIERTAS, por parte de los sujetos a que se refieren los artículos 1 de la Ley de Adquisiciones para el Distrito Federal y 1 de la Ley de Obras Públicas del Distrito Federal, así como de los licitantes, proveedores y contratistas, de conformidad con lo previsto en dichos ordenamientos legales.  </p>

<p>Asimismo tiene la finalidad de transparentar los contratos públicos que celebra como entidad administrativa con terceros y, consecuentemente, poner a disposición del público en general la información relativa al uso del gasto público.</p>

